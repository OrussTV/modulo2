const dato1 = prompt ("Escribe primer número")
const dato2 = prompt ("Escribe segundo número")

if (dato1 > dato2) {
    alert(`${dato2} Es menor que ${dato1}`)
}

if (dato1 < dato2) {
    alert(`${dato1} Es menor que ${dato2}`)
}

if (dato1 === dato2) {
    alert(`${dato1} Es igual que ${dato2}`)
}